    private void insertUserDetailsToProfile(){
        try{
            
            
            if (jTextField_firstName.getText().isEmpty() || jTextField_lastName.getText().isEmpty() || jTextField_platenumber.getText().isEmpty() || jTextField_vehicleType.getText().isEmpty()) {
                JOptionPane.showMessageDialog(null, "Please Fill in All Fields!");
            }
            else{
                
                String sql = "INSERT INTO profile"
                     +"(Firstname, Lastname, Gatepass_Type, Platenumber, Vehicle_Type)"
                     +"VALUES (?,?,?,?,?)";
                     
                     pStatement = serverString.prepareStatement(sql);
                     
                     pStatement.setString(1,jTextField_firstName.getText());
                     pStatement.setString(2,jTextField_lastName.getText());
                     pStatement.setString(3, (String) jComboBox_gatepassType.getSelectedItem());
                     pStatement.setString(4, jTextField_platenumber.getText());
                     pStatement.setString(5, jTextField_vehicleType.getText());
                      
                     pStatement.executeUpdate();
                     JOptionPane.showMessageDialog(null, "inserted successfully");

                     }
                
            }
            
                     catch(SQLException | HeadlessException ex){
                     JOptionPane.showMessageDialog(null, ex);
                     }
    }
