    public void transferTableDataToTextField(){
        try{
            
            int row = jTable1.getSelectedRow();
            String tblclick = (jTable1.getModel().getValueAt(row, 0).toString());
            String sql = "select * from profile where Barcode ="+tblclick+"";
            pStatement = serverString.prepareStatement(sql);
            rSet=pStatement.executeQuery();
            if(rSet.next())
            {
                String Barcode = String.valueOf(rSet.getInt("Barcode"));
                jTextField_barcode.setText(Barcode);
                String Firstname = String.valueOf(rSet.getString("Firstname"));
                jTextField_firstName.setText(Firstname);
                String Lastname = String.valueOf(rSet.getString("Lastname"));
                jTextField_lastName.setText(Lastname);
                String Gatepass_Type = String.valueOf(rSet.getString("Gatepass_Type"));
                jComboBox_gatepassType.setSelectedItem(Gatepass_Type);
                String Platenumber = String.valueOf(rSet.getString("Platenumber"));
                jTextField_platenumber.setText(Platenumber);
                String Vehicle_Type = String.valueOf(rSet.getString("Vehicle_Type"));
                jTextField_vehicleType.setText(Vehicle_Type);
                
                
                
            }
        }
        catch(Exception ex)
        {
            JOptionPane.showMessageDialog(null, ex);
        }
    }
    
    
    
    
    
    
    
    
    
    
    
